  private updateInterfaceInitialStep() {
    this.game.clearPossible();

    updatePageTitle(_('${you} must discard an item'), {});

    this.args.items.forEach((item) => {
      onClick(item.id, () => {
        this.updateInterfaceConfirm(item);
      });
    });

    addUndoButtons(this.args);
  }

  private updateInterfaceConfirm(item: MohoItemBase) {
    clearPossible();

    updatePageTitle(_('Discard ${itemName}?'), {
      itemName: getItem(item).name,
    });
    setSelected(item.id);

    addConfirmButton(() => {
      performAction('actDiscardItem', {
        itemId: item.id,
      });
    });
    addCancelButton();
  }

