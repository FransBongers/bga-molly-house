  public function stFestivityKnownThreatCheck()
  {
    $players = Players::getAll();

    $mollyHouses = Sites::getMany(MOLLY_HOUSES);

    foreach ($players as $player) {
      $revealedInformerToken = $player->getRevealedInformerToken();
      if ($revealedInformerToken === null) {
        continue;
      }
      $suit = $mollyHouses[$revealedInformerToken->getLocation()]->getSuit();

      $cardInReputation = $player->getCardsInReputation();
      foreach ($cardInReputation as $card) {
        if ($card->getSuit() === $suit) {
          $card->addToSafePile($player);
        }
      }
    }


    $this->resolveAction(['automatic' => true]);
  }


