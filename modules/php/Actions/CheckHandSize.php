  public function stCheckHandSize()
  {
    $player = $this->getPlayer();

    $hand = $player->getHand();

    if (count($hand) > 6) {
      $action = [
        'action' => ADD_EXCESS_CARDS_TO_GOSSIP,
        'playerId' => $player->getId(),
      ];
      $this->ctx->insertAsBrother(Engine::buildTree($action));
    }
    $this->resolveAction(['automatic' => true]);
  }


